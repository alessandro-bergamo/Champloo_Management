package com.champloo.bean;

public class CartItemBean {
	
	private int id_cart_item, cart, product_details, qnt_in_cart;
	

	public int getId_cart_item() {
		return id_cart_item;
	}

	public void setId_cart_item(int id_cart_item) {
		this.id_cart_item = id_cart_item;
	}

	public int getCart() {
		return cart;
	}

	public void setCart(int cart) {
		this.cart = cart;
	}

	public int getProduct_details() {
		return product_details;
	}

	public void setProduct_details(int product_details) {
		this.product_details = product_details;
	}

	public int getQnt_in_cart() {
		return qnt_in_cart;
	}

	public void setQnt_in_cart(int qnt_in_cart) {
		this.qnt_in_cart = qnt_in_cart;
	}
	
	
	

}
